/**
 * Strings Tipo de Caracteres
 * 
*/

console.log("Eduardo")


/**
 * Numbers
 * 33 -> Inteiro
 * 12.5 -> reais - float
 * NaN -> Not a Number
 * 
*/

console.log(33)
console.log(13 + 5)
console.log(13 / 5)

/**
 * Boolean
 * true
 * false
 * 
*/

console.log(true)
console.log(false)


/**
 * null => nulo
 * objeto que não possui nada dentro
 * diferente de indefinido (undefined)
 * 
*/
console.log(null)

/**
 * Object
 * Objeto
 * Propriedades / Atributos
 * Funcionalidade / Métodos
 * propriedade: valor
*/

console.log({
  name: "Edu",
  age: 30,
  andar: function() {
    console.log('andar')
  }
})

/**
 * Array (Vetores)
 * - Uma Lista
 * - Agrupamento de dados
 * ["Eduardo", 30]
 * 
*/
console.log(["Eduardo", 30])

